<?php

function get_era($year)
{
    $tiangan = [1 => '甲', 2 => '已', 3 => '丙', 4 => '丁', 5 => '戊', 6 => '己', 7 => '庚', 8 => '辛', 9 => '壬', 0 => '癸'];

    $dizhi = [1 => '子', 2 => '丑', 3 => '寅', 4 => '卯', 5 => '辰', 6 => '巳', 7 => '午', 8 => '未', 9 => '申', 10 => '酉', 11 => '戌', 0 => '亥'];

    $t = ($year - 3) % 10;
    $d = ($year - 3) % 12;

    return $tiangan[$t] . $dizhi[$d];
}
